"""
Calculadora de Soma com Entrada do Usuário


Leia 2 valores inteiros e armazene-os nas variáveis A e B. 

Efetue a soma de A e B, atribuindo o seu resultado à variável X. 

Entrada: A entrada contém 2 valores inteiros informados pelo usuário. 

Saída: Imprima a mensagem "X = " (letra X maiúscula) 
seguido pelo valor da variável X e pelo final de linha.

"""

# Calculadora de Soma com Entrada do Usuário
# Lê 2 valores inteiros e calcula a soma

# Leitura dos valores inteiros A e B
print("Digite o primeiro valor inteiro:")
A = int(input())

print("Digite o segundo valor inteiro:")
B = int(input())

# Efetua a soma de A e B, atribuindo o resultado à variável X
X = A + B

# Imprime o resultado no formato solicitado
print(f"X = {X}")
